package beans;

public class OfertaBean {

	private int idOferta;
	private int descuento;
	private String fechIni;
	private String fechFin;
	private String horaFin;
	private int cantAfec;
	private String foto;
	
	public int getIdOferta() {
		return idOferta;
	}
	public void setIdOferta(int idOferta) {
		this.idOferta = idOferta;
	}
	public int getDescuento() {
		return descuento;
	}
	public void setDescuento(int descuento) {
		this.descuento = descuento;
	}
	public String getFechIni() {
		return fechIni;
	}
	public void setFechIni(String fechIni) {
		this.fechIni = fechIni;
	}
	public String getFechFin() {
		return fechFin;
	}
	public void setFechFin(String fechFin) {
		this.fechFin = fechFin;
	}
	public String getHoraFin() {
		return horaFin;
	}
	public void setHoraFin(String horaFin) {
		this.horaFin = horaFin;
	}
	public int getCantAfec() {
		return cantAfec;
	}
	public void setCantAfec(int cantAfec) {
		this.cantAfec = cantAfec;
	}
	public String getFoto() {
		return foto;
	}
	public void setFoto(String foto) {
		this.foto = foto;
	}
	

	
	
}
